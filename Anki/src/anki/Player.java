package anki;


public class Player {

    private String name;
    private String ID;

    public Player() {
    }

    /***********SETTERS & GETTERS***********/
    
    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getID() {
        return this.ID;
    }
}
